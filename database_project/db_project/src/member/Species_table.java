package member;

public class Species_table {
	private String species_name;
	private double attack_p_comp;
	private double defend_p_comp;
	private double hp_comp;
	private double mp_comp;
	private double str_comp;
	private double int_comp;
	
	public String getSpecies_name() {
		return species_name;
	}
	public void setSpecies_name(String species_name) {
		this.species_name = species_name;
	}
	public double getAttack_p_comp() {
		return attack_p_comp;
	}
	public void setAttack_p_comp(double attack_p_comp) {
		this.attack_p_comp = attack_p_comp;
	}
	public double getDefend_p_comp() {
		return defend_p_comp;
	}
	public void setDefend_p_comp(double defend_p_comp) {
		this.defend_p_comp = defend_p_comp;
	}
	public double getHp_comp() {
		return hp_comp;
	}
	public void setHp_comp(double hp_comp) {
		this.hp_comp = hp_comp;
	}
	public double getMp_comp() {
		return mp_comp;
	}
	public void setMp_comp(double mp_comp) {
		this.mp_comp = mp_comp;
	}
	public double getStr_comp() {
		return str_comp;
	}
	public void setStr_comp(double str_comp) {
		this.str_comp = str_comp;
	}
	public double getInt_comp() {
		return int_comp;
	}
	public void setInt_comp(double int_comp) {
		this.int_comp = int_comp;
	}
}
